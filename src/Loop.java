public class Loop {
    public static void  main(String args[])
    {
      //For loop

      for(int i=1;i<=10;i++)
      {
          System.out.print(i + " ");
      }

      //While lopp

        int a = 100; //intitalize
        while (a<=110) //Condition
      {
          System.out.println(a);
          a++; //Steps

      }

        //for each loop
        //Array creation
        int num[]= {12,13,14,15};
        for(int x:num)
        {
            System.out.println(x);
        }
    }
}
